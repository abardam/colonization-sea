public class War {
    private int attacker;
    private int territory;

    public War(int attacker, int territory) {
        this.attacker = attacker;
        this.territory = territory;
    }

    public int getAttacker() {
        return attacker;
    }

    public int getTerritory() {
        return territory;
    }

    
}
