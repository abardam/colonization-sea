    //kind of a hacky way to get a country's code but WHATEVER
    private int getCountryNumber(String name)
    {
        int i=-1;

        if(name!=null)
        {
            for(String country:countries)
            {
                if(name.equals(country))
                {
                    return i;
                }
                i++;
            }
        }

        return -1;
        
    }
